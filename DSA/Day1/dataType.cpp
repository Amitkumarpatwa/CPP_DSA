#include<iostream>
using namespace std;
 int main()
 { 
    //integer data type
    int a =5;
    cout<<a<<endl;

    //char data type

    char c='b';
    cout<<c<<endl;

    //float data type
    float num=23.123;
    cout<<num<<endl;

    //double data type
    double d=23.123456789;
    cout<<d<<endl;

    //boolean data type
    bool n=false;
    cout<<n<<endl;

    cout<<sizeof(d)<<endl;


    return 0;
 }